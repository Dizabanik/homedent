function setNextCardRight() {
	if (currentPerson === numReviews) {
		currentPerson = 0;
		slide("left", currentPerson);
	} else {
		currentPerson++;
	}

	slide("right", currentPerson);
}

function setNextCardLeft() {
	if (currentPerson === 0) {
		currentPerson = numReviews;
		slide("right", currentPerson);
	} else {
		currentPerson--;
	}

	slide("left", currentPerson);
}

